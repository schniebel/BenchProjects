package com.hackerrank.beans;

public class JumpingOnCloudsForm {
	
	private int numberOfClouds;
	private String cloudSequenceInput;
	private int[] cloudSequence;
	
	public int getNumberOfClouds() {
		return numberOfClouds;
	}
	public void setNumberOfClouds(int numberOfClouds) {
		this.numberOfClouds = numberOfClouds;
	}
	public String getCloudSequenceInput() {
		return cloudSequenceInput;
	}
	public void setCloudSequenceInput(String cloudSequenceInput) {
		this.cloudSequenceInput = cloudSequenceInput;
	}
	public int[] getCloudSequence() {
		return cloudSequence;
	}
	public void setCloudSequence(int[] cloudSequence) {
		this.cloudSequence = cloudSequence;
	}
	
	

}
