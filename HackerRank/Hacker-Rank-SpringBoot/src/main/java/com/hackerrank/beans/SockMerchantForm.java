package com.hackerrank.beans;

public class SockMerchantForm {
	
	private int numberOfSocks;
	private String sockSequenceInput;
	private int[] sockSequence;
	
	
	public int getNumberOfSocks() {
		return numberOfSocks;
	}
	public void setNumberOfSocks(int numberOfSocks) {
		this.numberOfSocks = numberOfSocks;
	}
	public int[] getSockSequence() {
		return sockSequence;
	}
	public void setSockSequence(int[] sockSequence) {
		this.sockSequence = sockSequence;
	}
	public String getSockSequenceInput() {
		return sockSequenceInput;
	}
	public void setSockSequenceInput(String sockSequenceInput) {
		this.sockSequenceInput = sockSequenceInput;
	}
	
	

}
