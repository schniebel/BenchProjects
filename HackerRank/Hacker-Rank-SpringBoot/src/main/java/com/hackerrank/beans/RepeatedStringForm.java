package com.hackerrank.beans;

public class RepeatedStringForm {
	
	private long numberOfLetters;
	private String stringSequenceInput;
	
	
	public long getNumberOfLetters() {
		return numberOfLetters;
	}
	public void setNumberOfLetters(long numberOfLetters) {
		this.numberOfLetters = numberOfLetters;
	}
	public String getStringSequenceInput() {
		return stringSequenceInput;
	}
	public void setStringSequenceInput(String stringSequenceInput) {
		this.stringSequenceInput = stringSequenceInput;
	}
	
	
	

}
